def render_glyph(atlas, glyph_rows, col, row):
    """Stamp an 8x16 bitmap glyph into the atlas at grid (col, row), upscaled 2x."""
    x0 = col * CELL_W
    y0 = row * CELL_H
    for y, byte_val in enumerate(glyph_rows):
        for x in range(SRC_W):
            if byte_val & (0x80 >> x):
                for dy in range(SCALE):
                    for dx in range(SCALE):
                        atlas.putpixel((x0 + x * SCALE + dx, y0 + y * SCALE + dy),
                                       (255, 255, 255, 255))


def render_inverted_glyph(atlas, glyph_rows, col, row):
    """Stamp an inverted 8x16 glyph at grid (col, row), upscaled 2x."""
    x0 = col * CELL_W
    y0 = row * CELL_H
    for y, byte_val in enumerate(glyph_rows):
        for x in range(SRC_W):
            if byte_val & (0x80 >> x):
                # Foreground pixel → transparent (black) — already default
                pass
            else:
                # Background pixel → lit (white)
                for dy in range(SCALE):
                    for dx in range(SCALE):
                        atlas.putpixel((x0 + x * SCALE + dx, y0 + y * SCALE + dy),
                                       (255, 255, 255, 255))


def main():
    parser = argparse.ArgumentParser(description="Generate PDA ROM atlas for Williams Tube")
    parser.add_argument("--output", "-o", default="WilliamsTube_PDARom.png",
                        help="Output PNG filename (default: WilliamsTube_PDARom.png)")
    parser.add_argument("--no-copy", action="store_true",
                        help="Don't auto-copy to Unity project textures directory")
    args = parser.parse_args()

    output_path = os.path.join(SCRIPT_DIR, args.output)

    cp437 = build_cp437_font()
    icons = build_custom_icons()

    atlas = Image.new("RGBA", (ATLAS_W, ATLAS_H), (0, 0, 0, 0))

    # --- Indices 0-31: UI primitives ---
    print("Rendering indices 0-31 (UI primitives)...")
    for pda_idx, cp437_idx in UI_MAP.items():
        if cp437[cp437_idx] is not None:
            col = pda_idx % GRID_COLS
            row = pda_idx // GRID_COLS
            render_glyph(atlas, cp437[cp437_idx], col, row)

    # --- Indices 32-127: printable ASCII ---
    print("Rendering indices 32-127 (ASCII)...")
    for code in range(32, 128):
        if cp437[code] is not None:
            col = code % GRID_COLS
            row = code // GRID_COLS
            render_glyph(atlas, cp437[code], col, row)

    # --- Indices 128-159: custom PDA icons ---
    # Icons that should be rendered inverted (fg/bg swapped)
    inverted_icons = {144}
    print("Rendering indices 128-159 (custom icons)...")
    for pda_idx, bitmap in icons.items():
        col = pda_idx % GRID_COLS
        row = pda_idx // GRID_COLS
        if pda_idx in inverted_icons:
            render_inverted_glyph(atlas, bitmap, col, row)
        else:
            render_glyph(atlas, bitmap, col, row)

    # --- Indices 160-255: inverted ASCII (mirrors 32-127) ---
    print("Rendering indices 160-255 (inverted ASCII)...")
    for code in range(32, 128):
        inv_idx = code + 128  # 32→160, 127→255
        if cp437[code] is not None:
            col = inv_idx % GRID_COLS
            row = inv_idx // GRID_COLS
            render_inverted_glyph(atlas, cp437[code], col, row)

    atlas.save(output_path)
    print(f"\nSaved: {output_path}")
    print(f"  Size: {atlas.size[0]}x{atlas.size[1]} ({GRID_COLS}x{GRID_ROWS} grid, {CELL_W}x{CELL_H} per cell)")
    print(f"  Layout:")
    print(f"    0-31:    UI primitives (box-drawing, blocks, shades, arrows, icons)")
    print(f"    32-127:  Printable ASCII")
    print(f"    128-159: Custom PDA icons (signal, battery, lock, media, etc.)")
    print(f"    160-255: Inverted ASCII (fg/bg swapped)")

    # Auto-copy to Unity project
    if not args.no_copy and os.path.isdir(COPY_DEST_DIR):
        dest = os.path.join(COPY_DEST_DIR, args.output)
        shutil.copy2(output_path, dest)
        print(f"\nCopied to Unity: {dest}")
    elif not args.no_copy:
        print(f"\nUnity textures dir not found, skipping auto-copy: {COPY_DEST_DIR}")


if __name__ == "__main__":
    main()
